public class MovieTests {

	/**
	 * On passing a valid movie id
	 * I should expect back a 200 status code.
	 */

	/**
	 * On passing a invalid movie id
	 * I should expect back a 404 status code.
	 */

	/**
	 * On passing a valid movie id along with a valid language string
	 * I should expect back a 200 status code.
	 */

	/**
	 * On passing a valid movie id along with an invalid language string
	 * I should expect back a 404 status code.
	 */

	/**
	 * On passing a valid movie id
	 * I should should be able to retrieve and validate boolean adult field value.
	 */

	/**
	 * On passing a valid movie id
	 * I should should be able to retrieve and validate String title field value.
	 */

	/**
	 * On passing a valid movie id
	 * I should should be able to retrieve and validate String release_date field value.
	 */
}
